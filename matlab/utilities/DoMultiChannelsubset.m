	for j = 1:7
		if (j > 1)
			left = 144+(j-2)*120;
		else
			left = 15;
		end
		if (i==1)
			bottom = 471; height=90;
		elseif (i==2)
			bottom = 390; height=80;
		else
			bottom = 325; height=64;
		end
		pos = [left,bottom,109,height];
		haxc(i,j) = axes('Parent',hfig, ...
		'Units','pixels', ...
		'CameraUpVector',[0 1 0], ...
		'Color',[1 1 1], ...
		'Position',pos, ...
		'XColor',[0 0 0], ...
		'YColor',[0 0 0], ...
		'ZColor',[0 0 0]);
	end
end
