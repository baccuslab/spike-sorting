'ButtonDownFcn','DoMultiChanFunctions Unselect',...
'KeyPressFcn','DoMultiChanFunctions KeyTrap',...
'Name','Channel Grouping',...
'Tag','ChannelFig','doublebuffer','on','NumberTitle','off');
